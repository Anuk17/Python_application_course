#MY CALCULATOR PROGRAM
import math
def calc_add(num1, num2):
    return num1+num2
def calc_mul(num1, num2):
    return num1*num2
def calc_div(num1, num2):
    return num1/num2
def calc_sub(num1, num2):
    return num1-num2

def calc_mod(num1, num2):
    return math.sqrt(num1**2+num2**2)

